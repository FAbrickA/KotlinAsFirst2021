 * Сложная (4 балла)
 *
 * Перевести число, представленное цифровой строкой str,
 * из системы счисления с основанием base в десятичную.
 * Цифры более 9 представляются латинскими строчными буквами:
 * 10 -> a, 11 -> b, 12 -> c и так далее.
 * Например: str = "13c", base = 14 -> 250
 *
 * Использовать функции стандартной библиотеки, напрямую и полностью решающие данную задачу
 * (например, str.toInt(base)), запрещается.
 */
fun decimalFromString(str: String, base: Int): Int {
    val decimals = str.map { getDigitOfSymbol(it) }
    return decimal(decimals, base)
}

fun getDigitOfSymbol(symbol: Char): Int {
    if (symbol in '0'..'9') return symbol.code - '0'.code
    return symbol.code - 'a'.code + 10
}

/**
 * Сложная (5 баллов)
 *
 * Перевести натуральное число n > 0 в римскую систему.
 * Римские цифры: 1 = I, 4 = IV, 5 = V, 9 = IX, 10 = X, 40 = XL, 50 = L,
 * 90 = XC, 100 = C, 400 = CD, 500 = D, 900 = CM, 1000 = M.
 * Например: 23 = XXIII, 44 = XLIV, 100 = C
 */
fun roman(n: Int): String {
    var result = ""

    result += "M".repeat(n / 1000)

    result += getOneDigitRow(n / 100 % 10, "C", "D", "M")
    result += getOneDigitRow(n / 10 % 10, "X", "L", "C")
    result += getOneDigitRow(n % 10, "I", "V", "X")

    return result
}

fun getOneDigitRow(digit: Int, one: String, five: String, ten: String): String =
    when (digit) {
        0 -> ""
        in 1..3 -> one.repeat(digit)
        4 -> one + five
        in 5..8 -> five + one.repeat(digit - 5)
        9 -> one + ten
        else -> "" // incorrect digit
    }

/**
 * Очень сложная (7 баллов)
 *
 * Записать заданное натуральное число 1..999999 прописью по-русски.
 * Например, 375 = "триста семьдесят пять",
 * 23964 = "двадцать три тысячи девятьсот шестьдесят четыре"
 */
fun russian(n: Int): String {
    val thousandsList = threeDigitsRussianList(n / 1000 % 1000, isThousand = true)
    val lessThousandList = threeDigitsRussianList(n % 1000)
    val numberList = thousandsList + lessThousandList
    return numberList.joinToString(" ")
}

fun threeDigitsRussianList(n: Int, isThousand: Boolean = false): List<String> {
    val digits = if (!isThousand)
        listOf("", "один", "два", "три", "четыре", "пять", "шесть", "семь", "восемь", "девять")
    else
        listOf("", "одна", "две", "три", "четыре", "пять", "шесть", "семь", "восемь", "девять")

    val oneTenNumbers = listOf(
        "десять", "одиннадцать", "двенадцать", "тринадцать", "четырнадцать",
        "пятнадцать", "шестнадцать", "семнадцать", "восемнадцать", "девятнадцать",
    )
    val tenNumbers = listOf(
        "", "десять", "двадцать", "тридцать", "сорок",
        "пятьдесят", "шестьдесят", "семьдесят", "восемьдесят", "девяносто",
    )
    val hundredNumbers = listOf(
        "", "сто", "двести", "триста", "четыреста",
        "пятьсот", "шестьсот", "семьсот", "восемьсот", "девятьсот"
    )

    val list = mutableListOf<String>()
    val hundreds = n / 100 % 10
    val tens = n / 10 % 10
    val ones = n % 10

    // hundreds
    if (hundreds != 0) list.add(hundredNumbers[hundreds])
    // tens
    when {
        tens == 1 -> list.add(oneTenNumbers[ones])
        tens != 0 -> list.add(tenNumbers[tens])
    }
    // ones
    if (tens != 1 && ones != 0) list.add(digits[ones])

    // thousands ending
    if (isThousand && n > 0) {
        if (tens == 1) list.add("тысяч")
        else if (ones == 1) list.add("тысяча")
        else if (ones in 2..4) list.add("тысячи")
        else list.add("тысяч")
    }
    return list
}
